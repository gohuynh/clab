function write_config(handles,psd_info)
fid = fopen('config.txt','w');
fwrite(fid,get(handles.sampRateMenu,'Value'),'uint8');
fwrite(fid,get(handles.IRIGtimeMenu,'Value'),'uint8');
fwrite(fid,length(get(handles.latMagText,'String')),'uint8');
fwrite(fid,get(handles.latMagText,'String'),'char');
fwrite(fid,length(get(handles.longMagText,'String')),'uint8');
fwrite(fid,get(handles.longMagText,'String'),'char');
fwrite(fid,get(handles.latMenu,'Value'),'uint8');
fwrite(fid,get(handles.longMenu,'Value'),'uint8');
fwrite(fid,get(handles.IRIGtypeMenu,'Value'),'uint8');
fwrite(fid,get(handles.sensorMenu,'Value'),'uint8');
fwrite(fid,length(psd_info.sensRef),'uint8');
fwrite(fid,psd_info.sensRef,'char');
fwrite(fid,get(handles.statNameMenu,'Value'),'uint8');
fwrite(fid,length(psd_info.statName),'uint8');
fwrite(fid,psd_info.statName,'char');
fwrite(fid,length(get(handles.footerText,'String')),'uint8');
fwrite(fid,get(handles.footerText,'String'),'char');
fwrite(fid,get(handles.fileSizeTypeMenu,'Value'),'uint8');
fwrite(fid,length(psd_info.pn),'uint8');
fwrite(fid,psd_info.pn,'char');
fwrite(fid,length(get(handles.memQuantText,'String')),'uint8');
fwrite(fid,get(handles.memQuantText,'String'),'char');
fwrite(fid,get(handles.ai1Box,'Value'),'uint8');
fwrite(fid,get(handles.ai2Box,'Value'),'uint8');
fwrite(fid,get(handles.ai3Box,'Value'),'uint8');
fwrite(fid,get(handles.ai4Box,'Value'),'uint8');
fwrite(fid,get(handles.ai5Box,'Value'),'uint8');
fwrite(fid,get(handles.ai6Box,'Value'),'uint8');
fwrite(fid,get(handles.ai7Box,'Value'),'uint8');
fwrite(fid,get(handles.plotBox,'Value'),'uint8');
fwrite(fid,get(handles.fig1Menu,'Value'),'uint8');
fwrite(fid,get(handles.fig2Menu,'Value'),'uint8');
fwrite(fid,length(get(handles.ylowText,'String')),'uint8');
fwrite(fid,get(handles.ylowText,'String'),'char');
fwrite(fid,length(get(handles.yhighText,'String')),'uint8');
fwrite(fid,get(handles.yhighText,'String'),'char');
fclose(fid);